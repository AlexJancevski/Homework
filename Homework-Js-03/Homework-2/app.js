function dogToHumanYears(x) {
    let result = x * 7;
    console.log(`${result} years`);

    return result;
}

function reverse (x) {
    let result = x / 7;
    console.log(`${result} years`);

    return result;
}

dogToHumanYears(1);
dogToHumanYears(2);
dogToHumanYears(7);

reverse(7);
reverse(14);
reverse(49);